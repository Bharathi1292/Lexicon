class Mathmatics:
    @staticmethod
    def add(a, b):
        return a + b

    @staticmethod
    def multiply(a, b):
        return a * b

    @staticmethod
    def square(a):
        return a * a

print("Addition of numbers: ",Mathmatics.add(5, 3))       
print("Multiplication of numbers: ",Mathmatics.multiply(4, 6))   
print("Square of number: ",Mathmatics.square(7))       
