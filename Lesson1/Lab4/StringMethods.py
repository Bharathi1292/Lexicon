s = input("Enter a string : ")

Upper_s = s.upper()
print("String is converting to Uppercase : ", Upper_s)

Lower_s = s.lower()
print("String is converting to lowercase : ", Lower_s)

Length_s = len(s)
print("Number of characters in the String :", Length_s)

split_s = s.split()
print("Split into words : ", split_s)

replaced_s = s.replace("a", "o")
print("After replacing 'a' with 'o' in the String :", replaced_s)