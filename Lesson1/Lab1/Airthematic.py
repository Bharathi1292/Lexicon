number1 = int(input("Enter the first number: "))
number2 = int(input("Enter the second number: "))

#Sum
sum = number1 + number2
print(f"Sum of {number1} and {number2} is : {sum}")

#Difference
difference = number1 - number2
print(f"Difference of {number1} and {number2} is : {difference}")

#Product
product = number1 * number2
print(f"Product of {number1} and {number2} is : {product}")

#Division
division = number1 / number2
print(f"Division of {number1} and {number2} is : {division}")

#Power
power = number1 ** number2
print(f"Power of {number1} and {number2} is : {power}")
